#include <iostream>
using namespace std;

int main () {
    int fInt;
    int sInt;

    cout << "enter the first integer: ";
    cin >> fInt;
    cout << "enter the second integer: ";
    cin >> sInt;
    cout << fInt << " + " << sInt << " = " << fInt + sInt << endl;
    cout << fInt << " * " << sInt << " = " << fInt * sInt << endl;
    cout << fInt << " / " << sInt << " = " << fInt / sInt << endl;
    
    return 0;
}
